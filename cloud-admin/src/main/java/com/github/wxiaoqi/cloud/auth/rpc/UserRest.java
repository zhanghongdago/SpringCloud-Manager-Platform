package com.github.wxiaoqi.cloud.auth.rpc;

import com.ace.cache.annotation.Cache;
import com.github.wxiaoqi.cloud.auth.module.admin.service.PermissionService;
import com.github.wxiaoqi.cloud.auth.module.admin.vo.PermissionInfo;
import com.github.wxiaoqi.cloud.common.context.BaseContextHandler;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.*;

import java.util.List;

/**
 * ${DESCRIPTION}
 *
 * @author laogandie
 * @version 2017-06-21 8:15
 */
@RestController
@RequestMapping("api")
public class UserRest {
    @Autowired
    private PermissionService permissionService;

    @Cache(key="permission")
    @RequestMapping(value = "/permissions", method = RequestMethod.GET)
    public @ResponseBody
    List<PermissionInfo> getAllPermission(){
        return permissionService.getAllPermission();
    }

    @RequestMapping(value = "/user/permissions", method = RequestMethod.GET)
    public @ResponseBody
    List<PermissionInfo> getPermissionByUsername(){
        return permissionService.getPermissionByUsername(BaseContextHandler.getUsername());
    }
}
